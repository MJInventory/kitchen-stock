import { createAppUserApi } from "./app-user-api.js";
import { createSetupAdminApi } from "./setup-admin-api.js";
import { createOperationsApi } from "./operations-api.js";
import { createMutationApi } from "./mutation-api.js";
import { createWorkflowApi } from "./workflow-api.js";

export function createServerApiHandlers({
  editableUserSources,
  normalizeRole,
  publicUser,
  canChangeAppUserRole,
  canDeleteAppUserRecord,
  bcrypt,
  hasPostgres,
  pushEnabled,
  vapidPublicKey,
  readJson,
  send,
  requireUser,
  requireRole,
  storeSession,
  findAppUserByName,
  changeOwnPassword,
  refreshUserFromDirectory,
  pgRecordSuccessfulLogin,
  pgGetOwnSettings,
  pgUpdateOwnSettings,
  pgSavePushSubscription,
  pgRemovePushSubscription,
  getAppUsers,
  createAppUser,
  findAppUserById,
  updateAppUser,
  deleteAppUser,
  getItems,
  listOpenRequests,
  listStandingOrders,
  getDashboardSummary,
  pgListNotificationsForUser,
  pgMarkNotificationsRead,
  listDriverSheet,
  assignDriverToSheet,
  listReceivingSheet,
  pgSaveSupplierDeliveryNote,
  listOrderReport,
  getDailyGuestCount,
  saveDailyGuestCount,
  getManagementReport,
  listKitchenRoster,
  saveKitchenRoster,
  setKitchenRosterLocked,
  listKitchenShiftTypesAdmin,
  saveKitchenShiftType,
  listInternalDataServices,
  getInternalDataServiceDetail,
  listInternalDataServicesForExport,
  saveInternalDataService,
  deleteInternalDataService,
  itemFormOptions,
  listStorageLocationsAdmin,
  listCategoriesAdmin,
  listUnitsOfMeasureAdmin,
  listSuppliersAdmin,
  listShelfCodesAdmin,
  saveStorageLocation,
  saveCategory,
  deleteCategory,
  saveUnitOfMeasure,
  deleteUnitOfMeasure,
  saveShelfCode,
  saveSupplier,
  deleteSupplier,
  updateItemSettings,
  deleteInventoryItem,
  createInventoryItem,
  createStockCount,
  createInvoiceCapture,
  createInvoiceLine,
  listOcrRules,
  createOcrRule,
  emailInvoicePicture,
  ocrSpaceParseImage,
  updateRequest,
  deliverRequest,
  deliverStandingOrderRunLine,
  updateStandingOrderRunLine,
  undoDeliveredStandingOrderRunLine,
  undoDeliveredRequest,
  restoreDeletedRequestFromAudit,
  updateDriverLine,
  deliverDriverLine,
  canDeleteRequest,
  deleteRequest,
  deleteStandingOrderRunLine,
  getRequests,
  brevoApiKey,
  mailFrom,
  accountingInbox,
  smtpHost,
  smtpUser,
  smtpPass,
  isRender,
  metrics,
  cache,
  createRequest,
  createRequestsBatch,
  pgListInternalOrders,
  pgCreateInternalOrder,
  pgUpdateInternalOrderRequest,
  pgUpdateInternalOrderPicking,
  createStandingOrder,
  listStandingOrderRuns,
  ensureStandingOrdersForDate,
  updateStandingOrderRecord,
  pgDeleteStandingOrder
}) {
  function publicUserForAdmin(user, actor = null) {
    const editable = editableUserSources.has(user.source);
    const canEditRole = actor ? canChangeAppUserRole(actor, user, user.role) : true;
    return {
      ...publicUser(user),
      id: user.id || "",
      lastLoginAt: user.lastLoginAt || "",
      isDriver: Boolean(user.isDriver),
      isPicker: Boolean(user.isPicker),
      isKitchenStaff: Boolean(user.isKitchenStaff),
      kitchenFunction: user.kitchenFunction || "",
      notifyOnNewOrders: Boolean(user.notifyOnNewOrders),
      notifyOnDelivery: user.notifyOnDelivery !== false,
      notifyAreas: {
        bar: user.notifyAreas?.bar !== false,
        foh: user.notifyAreas?.foh !== false,
        kitchen: user.notifyAreas?.kitchen !== false,
        general: user.notifyAreas?.general !== false
      },
      editable,
      canEditRole,
      canSave: editable && (canEditRole || normalizeRole(user.role) === "user" || normalizeRole(user.role) === "power-user"),
      canDelete: actor ? canDeleteAppUserRecord(actor, user) && String(actor.name || "").toLowerCase() !== String(user.name || "").toLowerCase() : false
    };
  }

  return {
    handleAppUserApi: createAppUserApi({
      bcrypt,
      hasPostgres,
      pushEnabled,
      vapidPublicKey,
      readJson,
      send,
      requireUser,
      requireRole,
      storeSession,
      publicUser,
      publicUserForAdmin,
      canChangeAppUserRole,
      canDeleteAppUserRecord,
      findAppUserByName,
      changeOwnPassword,
      refreshUserFromDirectory,
      pgRecordSuccessfulLogin,
      pgGetOwnSettings,
      pgUpdateOwnSettings,
      pgSavePushSubscription,
      pgRemovePushSubscription,
      getAppUsers,
      createAppUser,
      findAppUserById,
      updateAppUser,
      deleteAppUser
    }),
    handleOperationsApi: createOperationsApi({
      requireUser,
      requireRole,
      readJson,
      send,
      getItems,
      listOpenRequests,
      listStandingOrders,
      getDashboardSummary,
      pgListNotificationsForUser,
      pgMarkNotificationsRead,
      listDriverSheet,
      assignDriverToSheet,
      listReceivingSheet,
      pgSaveSupplierDeliveryNote,
      listOrderReport,
      getDailyGuestCount,
      saveDailyGuestCount,
      getManagementReport,
      listKitchenRoster,
      saveKitchenRoster,
      setKitchenRosterLocked,
      listKitchenShiftTypesAdmin,
      saveKitchenShiftType,
      bcrypt,
      findAppUserByName,
      listInternalDataServices,
      getInternalDataServiceDetail,
      listInternalDataServicesForExport,
      saveInternalDataService,
      deleteInternalDataService
    }),
    handleSetupAdminApi: createSetupAdminApi({
      requireUser,
      requireRole,
      readJson,
      send,
      itemFormOptions,
      listStorageLocationsAdmin,
      listCategoriesAdmin,
      listUnitsOfMeasureAdmin,
      listSuppliersAdmin,
      listShelfCodesAdmin,
      saveStorageLocation,
      saveCategory,
      deleteCategory,
      saveUnitOfMeasure,
      deleteUnitOfMeasure,
      saveShelfCode,
      saveSupplier,
      deleteSupplier
    }),
    handleMutationApi: createMutationApi({
      requireUser,
      requireRole,
      readJson,
      send,
      updateItemSettings,
      deleteInventoryItem,
      createInventoryItem,
      createStockCount,
      createInvoiceCapture,
      createInvoiceLine,
      listOcrRules,
      createOcrRule,
      emailInvoicePicture,
      ocrSpaceParseImage,
      updateRequest,
      deliverRequest,
      deliverStandingOrderRunLine,
      updateStandingOrderRunLine,
      undoDeliveredStandingOrderRunLine,
      undoDeliveredRequest,
      restoreDeletedRequestFromAudit,
      updateDriverLine,
      deliverDriverLine,
      canDeleteRequest,
      deleteRequest,
      deleteStandingOrderRunLine
    }),
    handleWorkflowApi: createWorkflowApi({
      requireUser,
      requireRole,
      readJson,
      send,
      getItems,
      getRequests,
      hasPostgres,
      brevoApiKey,
      mailFrom,
      accountingInbox,
      smtpHost,
      smtpUser,
      smtpPass,
      isRender,
      metrics,
      cache,
      createRequest,
      createRequestsBatch,
      pgListInternalOrders,
      pgCreateInternalOrder,
      pgUpdateInternalOrderRequest,
      pgUpdateInternalOrderPicking,
      createStandingOrder,
      listStandingOrders,
      listStandingOrderRuns,
      ensureStandingOrdersForDate,
      updateStandingOrderRecord,
      pgDeleteStandingOrder
    })
  };
}
